document.getElementById('playButton').addEventListener('click', function() {
    var audio = document.getElementById('birthdayAudio');
    var video = document.getElementById('birthdayVideo');
    audio.play();
    video.play();
    showConfetti();
});

function showConfetti() {
    var duration = 5 * 1000;
    var end = Date.now() + duration;

    (function frame() {
        confetti({
            particleCount: 5,
            angle: 60,
            spread: 55,
            origin: { x: 0 }
        });
        confetti({
            particleCount: 5,
            angle: 120,
            spread: 55,
            origin: { x: 1 }
        });

        if (Date.now() < end) {
            requestAnimationFrame(frame);
        }
    }());
}

